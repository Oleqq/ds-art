    private function upsertCategory(
        string $name,
        string $icon,
        ?KnowledgeCategory $parent,
        int $sortOrder,
        ?int $adminId,
    ): KnowledgeCategory {
        return KnowledgeCategory::query()->updateOrCreate(
            ['slug' => $this->makeCategorySlug($name, $parent)],
            [
                'parent_id' => $parent?->id,
                'name' => $name,
                'icon' => $icon,
                'sort_order' => $sortOrder,
                'is_visible_to_employees' => true,
                'created_by' => $adminId,
                'updated_by' => $adminId,
            ],
        );
    }

    private function makeCategorySlug(string $name, ?KnowledgeCategory $parent): string
    {
        $prefix = $parent?->slug ? "{$parent->slug}-" : '';

        return Str::slug($prefix.$name) ?: Str::random(8);
    }

    private function makeUniqueArticleSlug(string $title): string
    {
        return Str::slug($title) ?: Str::random(8);
    }
