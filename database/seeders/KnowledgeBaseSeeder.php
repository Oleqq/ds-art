                            'summary' => $articlePayload['summary'],
                            'content' => $articlePayload['content'],
                            'blocks' => $blocks,
                            'sort_order' => $articleIndex,
                            'is_published' => true,
                            'created_by' => $adminId,
                            'updated_by' => $adminId,
                        ],
                    );

                    app(KnowledgeArticleStructureSync::class)->sync($article);
                }
            }
        }
